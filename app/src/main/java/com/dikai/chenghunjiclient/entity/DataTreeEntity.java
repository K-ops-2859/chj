package com.dikai.chenghunjiclient.entity;

import java.util.List;

/**
 * Created by cmk03 on 2017/12/11.
 */

public class DataTreeEntity<K, V> {

    private K key;
    private List<V> value;

    public DataTreeEntity(K key, List<V> value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public List<V> getValue() {
        return value;
    }
}
