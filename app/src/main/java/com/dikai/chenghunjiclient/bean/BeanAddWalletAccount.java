public class BeanAddWalletAccount {
    private String FacilitatorID;
    private int SourceType;//0银行卡,1支付宝
    private String Number;
    private String AccountName;
    private String OpeningBank;
    private String Phone;
    private String AffiliatedBank;
    private int Type;

    public BeanAddWalletAccount(String facilitatorID, int sourceType, String number, String accountName,
                                String openingBank, String phone, String affiliatedBank, int type) {
        FacilitatorID = facilitatorID;
        SourceType = sourceType;
        Number = number;
        AccountName = accountName;
        OpeningBank = openingBank;
        Phone = phone;
        AffiliatedBank = affiliatedBank;
        Type = type;
    }
}
