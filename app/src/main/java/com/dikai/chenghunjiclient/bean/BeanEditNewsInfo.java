public class BeanEditNewsInfo {
    private String UserID;
    private String Name;
    private String DateOfBirth;
    private String PlaceOfOrigin;
    private String Constellation;
    private String Occupation;
    private String Phone;
    private String QQNumber;
    private String WechatNumber;

    public BeanEditNewsInfo(String userID, String name, String dateOfBirth,
                            String placeOfOrigin, String constellation, String occupation,
                            String phone, String QQNumber, String wechatNumber) {
        UserID = userID;
        Name = name;
        DateOfBirth = dateOfBirth;
        PlaceOfOrigin = placeOfOrigin;
        Constellation = constellation;
        Occupation = occupation;
        Phone = phone;
        this.QQNumber = QQNumber;
        WechatNumber = wechatNumber;
    }
}
